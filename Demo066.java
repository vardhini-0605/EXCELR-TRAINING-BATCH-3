public class Demo066 {
    public static void main(String[] args) {
        
        int[] numbers = {10, 20, 30, 40, 50};
        
        System.out.println("Index values of the array:");
        for (int i = 0; i < numbers.length; i++) {
            System.out.println("Index: " + i + " = Value: " + numbers[i]);
        }
    }
}
